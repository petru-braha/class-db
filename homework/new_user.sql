DECLARE
  v_user VARCHAR2(30);
BEGIN

v_user:= 'adi';
CREATE USER v_user IDENTIFIED BY v_user
   DEFAULT TABLESPACE USERS
   TEMPORARY TABLESPACE TEMP;

ALTER USER v_user
   QUOTA 100M ON USERS;

GRANT CONNECT TO v_user;

GRANT
   CREATE TABLE
TO v_user;

GRANT
   CREATE VIEW
TO v_user;

GRANT
   CREATE SEQUENCE
TO v_user;

GRANT
   CREATE TRIGGER
TO v_user;

GRANT
   CREATE SYNONYM
TO v_user;

GRANT
   CREATE PROCEDURE
TO v_user;

GRANT
   CREATE TYPE
TO v_user;
GRANT SELECT_CATALOG_ROLE TO v_user;
GRANT EXECUTE_CATALOG_ROLE TO v_user;
  END;
